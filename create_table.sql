  channel_name VARCHAR(60) NOT NULL,
  category VARCHAR(30) NOT NULL,
  subscribers FLOAT,
	audience_country VARCHAR(30), avg_views FLOAT,avg_likes FLOAT, avg_comments FLOAT);
	
	

